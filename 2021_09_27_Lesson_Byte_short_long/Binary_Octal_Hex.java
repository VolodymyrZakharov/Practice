package Lesson_4_Int_Long_Short;

import java.security.PublicKey;

public class Binary_Octal_Hex {
    public static void main(String[] args) {
        int hex1 = 0xABC; // Шестнадцатеричная система счисления
        int hex2 = 0x100; // Шестнадцатеричная система счисления
        int binary3 = 0b00101011; // Двоичная система счисления
        int octal4 = 32; // восьмеричная система счисления
        System.out.println(hex1);
        System.out.println(hex2);
        System.out.println(binary3);
        System.out.println(octal4);
    }
}
