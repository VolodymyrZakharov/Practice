public class Boolean {
    public static void main(String[] args) {
        int number = 0;

        if (number > 0) {
            System.out.println("The number " + number + " is positive");
        } else if (number < 0) {
            System.out.println("The number " + number + " is not negative");
        } else {
            System.out.println("The number is 0");
        }
    }
}
